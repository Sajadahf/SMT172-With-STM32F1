void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
	{
		if (Toggle_Captured==0)
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			Toggle_Captured = 1;

			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Toggle_Captured==1)
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			__HAL_TIM_SET_COUNTER(htim, 0);

			if (IC_Val2 > IC_Val1)
			{

				if (counter > 7) counter = 0;
				DC[counter] = ((float)(IC_Val2-IC_Val1)/((float)IC_Val2));
				counter ++ ;

				DC_Avg = (DC[0]+DC[1]+DC[2]+DC[3]+DC[4]+DC[5]+DC[6]+DC[7])/8;
				Temp10 = ((DC_Avg-0.32)/0.0047)*10;
				Temp20 = (uint32_t)(Temp10);
				Temp = (float)Temp20 / 10;


			}

			Toggle_Captured = 0;


			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);

		}
	}
}
